    //metodo para excluir uma OS
    private void excluirOS() {

        int confirma = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir essa OS ?", "Atenção", JOptionPane.YES_NO_OPTION);

        if (confirma == JOptionPane.YES_OPTION) {

            String sql = "delete from tbos where os=?";

            try {
                pst = conexao.prepareStatement(sql);

                pst.setString(1, txtOS.getText());
                int apagado = pst.executeUpdate();
                if (apagado > 0) {
                    JOptionPane.showMessageDialog(null, "OS Exluida com Sucesso");

                    txtOS.setText(null);
                    txtData.setText(null);
                    txtCliId.setText(null);
                    txtOsEquip.setText(null);
                    txtOsDef.setText(null);
                    txtOsServ.setText(null);
                    txtOsTec.setText(null);
                    txtOsValor.setText(null);

                    //habilitar os objetos
                    btnOsAdicionar.setEnabled(true);
                    txtCliPesquisar.setEnabled(true);
                    tblClientes.setVisible(true);
                }

            } catch (Exception e) {
                // mostra uma janela com a excessão
                JOptionPane.showMessageDialog(null, e);
            }

        }

    }

    //metodo para imprimir uma OS
    private void imprimirOS() {

        //gerando um relatorios de clientes 
        //aparece uma janela para o usuario confirmar a impressão
        int confirma = JOptionPane.showConfirmDialog(null, "confirma a impressão dessa OS?", "Atenção", JOptionPane.YES_NO_OPTION);

        if (confirma == JOptionPane.YES_OPTION) {
            //emitindo o relatorio com o framework jasperReport
            try {
                //usando a class Hashmap para criar um filtro 
                HashMap filtro = new HashMap();
                filtro.put("os", Integer.parseInt(txtOS.getText())); //faz a conversão de String para inteiro
                // os é o nome do arquivo do IReport

                //usando a classe jasperPrint para preparar a impressão de um relatorio
                JasperPrint print = JasperFillManager.fillReport("C:\\Users\\Harrison\\Documents\\reports\\os.jasper", filtro, conexao);

                // a linha abaixo exibe o relatorio atraves da classe JasperViewer
                JasperViewer.viewReport(print, false);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

    }

